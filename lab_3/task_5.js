/*
Створити змінну «resultArray» (масив). 
Створити змінні «first» = 1, «second» = 2, «senseOfLife» = 42. 
За допомогою методу масива .push() додати в масив спочатку «first» потім «second». 
Після цього, за допомогою методу .unshift() додати змінну «senseOfLife».
 */

let resultArray = [];
let first = 1;
let second = 2;
let senseOfLife = 42;
resultArray.push(first);
resultArray.push(second);
resultArray.unshift(senseOfLife);